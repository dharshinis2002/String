public class ConcatenatetwoString {
    public static void main(String[] args) {
        String name="dharshini";
        String name2="siva";

        System.out.println("String 1:"+name);
        System.out.println("String 2:"+name2);

        //concatenating two string
        String name3=name.concat(name2);

        System.out.println("the concatenating String is: "+name3);
    }
}
